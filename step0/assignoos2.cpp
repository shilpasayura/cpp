// Logical Operators ( &&, ||, ! )

#include <iostream>
using namespace std;

int main(){

	cout << "\n Logical AND ( && ) Operator.";
	cout << "\n ((10<20)&&(30<40)) : " << ((10<20)&&(30<40));
	cout << "\n ((10<20)&&(30>40)) : " << ((10<20)&&(30>40));
	cout << "\n ((10>20)&&(30<40)) : " << ((10>20)&&(30<40));
	cout << "\n ((10>20)&&(30>40)) : " << ((10>20)&&(30>40));

	cout << "\n\n Logical OR ( || ) Operator.";
	cout << "\n ((10<20)||(30<40)) : " << ((10<20)||(30<40));
	cout << "\n ((10<20)||(30>40)) : " << ((10<20)||(30>40));
	cout << "\n ((10>20)||(30<40)) : " << ((10>20)||(30<40));
	cout << "\n ((10>20)||(30>40)) : " << ((10>20)||(30>40));

	cout << "\n\n Logical NOT ( ! ) Operator.";
	cout << "\n ( !(10<20)) : " << ( !(10<20));
	cout << "\n ( !(10>20)) : " << ( !(10>20));

	getchar();
}
