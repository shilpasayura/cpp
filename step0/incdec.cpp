#include <iostream>
using namespace std;

int main() {
//int divided by int type 
cout << " 7/2 is " << 7/2 << endl; 

//float divided by int type 
cout << " 7.0/2 is " << 7.0/2 << endl; 

//int divided by float type 
cout << " 7/2.0 is " << 7/2.0 << endl; 

//float divided by float type 
cout << " 7.0/2.0 is " << 7/2.0 << endl; 

cout << " 7/2 is " << 7.0/2 << endl; 

}